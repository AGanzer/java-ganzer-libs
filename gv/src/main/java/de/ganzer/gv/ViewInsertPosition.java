package de.ganzer.gv;

/**
 * The ViewInsertPosition enumeration defines the position where to insert a
 * view into a group.
 *
 * @see View#insertView(View)
 */
public enum ViewInsertPosition {
    /**
     * The view is inserted in front of the target view.
     */
    AFTER,
    /**
     * The view is inserted under the target view.
     */
    BEFORE
}
